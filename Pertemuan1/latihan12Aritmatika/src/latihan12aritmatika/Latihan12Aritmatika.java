package latihan12aritmatika;


public class Latihan12Aritmatika {

    public static void main(String[] args) {

        int a = 10;
        int b = 25;
        int c = 15;
        int d = 20;

        System.out.printf("a + b = %d\r\n", (a + b));
        System.out.printf("a - b = %d\r\n", (a - b));
        System.out.printf("a * b = %d\r\n", (a * b));
        System.out.printf("b / a = %d\r\n", (b / a));
        System.out.printf("b %% a = %d\r\n", (b % a));
        System.out.printf("c %% a = %d\r\n", (c % a));

        System.out.printf("a++ = %d\r\n", (a++));
        System.out.printf("b-- = %d\r\n", (a--));
        System.out.printf("d++ = %d\r\n", (d++));
        System.out.printf("++d = %d\r\n", (++d));

    }
}
